  <h4>RYS HISTORYCZNY KOŁA ŁOWIECKIEGO NR 87 HUBERTUS W NOWEM</h4>

  <p>
    Po zakończeniu II wojny światowej, w październiku 1945r, powołana Tymczasowa
    Powiatowa Rada Łowiecka w Świeciu z Tymczasowym Łowczym Powiatowym inż.
    Obniskim, wystosowała ogłoszenie adresowane do Zarządów Miejskich, Zarządów
    Gmin oraz Nadleśnictw Państwowych powiatu świeckiego. W treści ogłoszenia
    zawarto kwestie prawne obowiązujących przepisów i Statutu PZŁ dotyczące
    uprawnień do posiadania broni myśliwskiej oraz członkostwa w Polskim Związku
    Łowieckim. W dniu 15 maja 1946r do Nowego, przybył delegat Referatu
    Łowiectwa Powiatowej Rady Narodowej w Świeciu pan Lewandowski oraz
    przedstawiciel Lasów Państwowych pan Lik. Wymienieni w oparciu o założenia
    Polskiego Związku Łowieckiego mieli zorganizować zebranie założycielskie
    Koła w Nowem. Zebranie to odbyło się 16 maja 1946r, na którym to podjęto
    uchwałę o utworzeniu Koła Łowieckiego w Nowem. Wybrano zarząd, którego
    pierwszym prezesem został Zbigniew Urbańczyk, członek przedwojennego
    Polskiego Związku Stowarzyszeń Łowieckich. W dniu 20 sierpnia 1946r Koło
    zawarło umowę na dzierżawę terenów łowieckich obejmujących obszary ówczesnej
    gminy Nowe, za wyjątkiem lasów państwowych. Łączny obszar terenów wyniósł
    5,000 ha. Zarząd Koła w dniu 14 września 1946r wysłał wniosek do Pomorskiej
    Rady Łowieckiej w Toruniu, z prośbą o przyjęcie Koła do Polskiego Związku
    Łowieckiego, z nazwą „Rejonowe Koło Łowieckie w Nowem nad Wisłą”. Wniosek
    został pozytywnie rozpatrzony i od tej pory Koło posiadało moc prawną. W
    dniu 9 lipca 1949r Koło przyjęło nazwę „Hubertus” a w roku 1953 nastąpiło
    połączenie kół łowieckich z Nowego i Warlubia, będącego pierwszym od 1945r
    prawnie zorganizowanym Kołem Łowieckim w powiecie świeckim. W wyniku tego
    połączenia, zwiększyła się powierzchnia terenów łowieckich, ponieważ doszły
    znaczne obszary leśne. Koło przyjęło wówczas nazwę „Koło Łowieckie Hubertus
    w Warlubiu”. W roku 1958 nastąpiła dalsza reorganizacja Koła, powstała na
    skutek zmian obszarów obwodów łowieckich. Siedzibę koła przeniesiono do
    Nowego, a nazwę zmieniono na Koło Łowieckie Hubertus Nowe. W roku 1996 koło
    ufundowało sztandar, którego rozwinięcie i poświęcenie nastąpiło na mszy św.
    hubertowskiej w Nowem, w 50-rocznicę istnienia Koła. Z okazji tej
    umundurowano również wszystkich członków Koła oraz opracowano i wydrukowano
    monografię Koła w twardej oprawie, którą otrzymał każdy członek Koła i osoby
    zasłużone. Koło za swe osiągnięcia podczas jubileuszu 50 -lecia zostało
    odznaczone Złotym Medalem Zasługi Łowieckiej. Obecnie Koło Łowieckie posiada
    nadany numer ewidencyjny 87 i podlega pod Zarząd Okręgowy Polskiego Związku
    Łowieckiego w Bydgoszczy a oficjalna nazwa to Koło Łowieckie nr 87
    „Hubertus” w Nowem.
  </p>

